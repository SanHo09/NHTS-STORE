    public void loadInvoice() {
        txtOrder.setText("*************************************************************Invoice Detail**************************************************************\n"
                +"****************************************************************************************************************************************\n"
                +"Item Name:\t\t\t\t" + "Number \t\t\t" + "Price($)\n");
        orderDetails.forEach(orderDetail -> {
            txtOrder.setText(txtOrder.getText()
                    + orderDetail.getProduct().getName()
                    + "\t\t\t"
                    + orderDetail.getQuantity()
                    + "\t\t\t"
                    +  orderDetail.getProduct().getSalePrice() * orderDetail.getQuantity() + "\n");
            
        double totalPrice = orderDetails.stream()
            .mapToDouble(od -> od.getProduct().getSalePrice()* od.getQuantity())
            .sum();
        
        lblTotalPrice.setText("Total sale price: " + totalPrice + " $");   
        lblTranportFee.setText("Tranport fee: " + 0 + "$");
        lblTotal.setText("Total: " + totalPrice + " $");
        });
    }

    public List<InvoiceDetail> mapOrderDetailsToInvoiceDetails(List<OrderDetail> orderDetails, Invoice invoice) {
        List<InvoiceDetail> invoiceDetails = new ArrayList<>();
        for (OrderDetail od : orderDetails) {
            InvoiceDetail invoiceDetail = new InvoiceDetail();
            invoiceDetail.setInvoice(invoice); // establish bidirectional relationship
            invoiceDetail.setProduct(od.getProduct());
            invoiceDetails.add(invoiceDetail);
        }
        return invoiceDetails;
    }

    public Customer getCustomer() {
        Customer customer = new Customer();
        customer.setName(txtCustomerName.getText());
        customer.setEmail(txtEmail.getText());
        customer.setPhoneNumber(txtPhoneNumber.getText());
        customer.setAddress(txtAddress.getText());
        return customer;
    }
    
