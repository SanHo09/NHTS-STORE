    private void btnBuyNowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuyNowMouseClicked
        Invoice invoice = new Invoice();
        invoice.setCreateDate(new Date());
        var customer = getCustomer();
        var invoiceDetails = mapOrderDetailsToInvoiceDetails(orderDetails, invoice);
        invoice.setInvoiceDetail(invoiceDetails);
        invoice.setCustomer(customer);

        customerService.save(customer);
        invoiceService.save(invoice);
        orderService.remove(existingOrder.getId());
        RouteParams params = new RouteParams();
        navigationService.navigateTo(TransactionCompletedPanel.class, params);
    }//GEN-LAST:event_btnBuyNowMouseClicked

